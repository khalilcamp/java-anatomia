// odo arquivo java deve iniciar tendo letra maiúscula. 
// Se for composta, a segunda palavra deve ser maiuscula.
// Obviamente, o nome da classe deve ser o MESMO do arquivo.
public class AnatomiaDois {
    // Quando usar um nome maiúsculo, não deve ser alterado.
    // Final define estado final e que não pode alterar.
    // Apenas maiuscula já deixa implicito o final.
    final String BR = "Brasil";
    // Para declaração de variáveis é necessário utilização de caracteres
    // numeros ou simbolos, porém com pequenas regras.
    // Podemos ter o inicio de declarações, como:
    String _Nome = "João";
    String $Sobrenome = "Silva";
    String nomeCompleto = "João Silva";
    // Em caso de querer dar nome maiusculo e não quiser que seja final
    // use o _ ou $.
    // De resto, apenas minusculas.
}
