public class CondicionaisSDois {
    public static void main(String[] args) {
        // Condicionais complexas provém se não apenas um resultado verdadeiro
        // Mas falso também.

        int nota = 6;

        if(nota >= 7){
            System.out.println("Aprovado");
        } else {
            System.out.println("Reprovado");
        }
    }
}
